CREATE TABLE [dbo].[Posts_Reports]
(
    [Id] BIGINT NOT NULL PRIMARY KEY IDENTITY,
    [AccountId] BIGINT NOT NULL DEFAULT 0 ,
    [PostId] BIGINT NOT NULL DEFAULT 0 ,
    [Reason] TINYINT NOT NULL DEFAULT 0,
    [ReasonText] NVARCHAR(1024) NULL,
    [ModifiedTime]  DATETIMEOFFSET NOT NULL DEFAULT '1900-01-01 00:00:00 +00:00',
    [CreatedTime]  DATETIMEOFFSET NOT NULL DEFAULT '1900-01-01 00:00:00 +00:00',
)